package cn.com.bettle.net.entity;

public class Res {

	private String resId;
	private String resName;
	private String resTable;
	private String resTerm;
	private String intro;

	public String getResId() {
		return resId;
	}

	public void setResId(String resId) {
		this.resId = resId;
	}

	public String getResName() {
		return resName;
	}

	public void setResName(String resName) {
		this.resName = resName;
	}

	public String getResTable() {
		return resTable;
	}

	public void setResTable(String resTable) {
		this.resTable = resTable;
	}

	public String getResTerm() {
		return resTerm;
	}

	public void setResTerm(String resTerm) {
		this.resTerm = resTerm;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

}
